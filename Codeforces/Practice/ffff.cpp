#include<bits/stdc++.h>
using namespace std;
int main()
{
    int q;
    cin>>q;
    while(q--)
    {
       int n;
       cin>>n;
       string s;
       cin>>s;
       for(int i=0;i<n;i++)
       {
           if(s[i+1]=='a' || s[i+1]=='e')
           {
               s.insert();
           }
       }
       cout<<s<<endl;
    }
    return 0;
}

